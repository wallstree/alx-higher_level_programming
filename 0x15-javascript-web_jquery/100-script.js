window.addEventListener('DOMContentLoaded', (event) => {
  document.querySelector('header').style.color = '#FF0000';
});
/*
 * The window.addEventListener() function is called. This function adds an event listener to the DOMContentLoaded event of the window object. This event is fired when the HTML document has been completely loaded and parsed, and all the DOM elements are available for manipulation.
* Inside the event listener, we use the document.querySelector() function to select the header element.
* We set the color style property of the header element to #FF0000, which is the hexadecimal value for the color red.
*/
